// 146개 구절 → 기준 윤년(2000년) 366일 날짜로 매핑
// 실행: 프로젝트 루트에서 dart database/seed_daily_verse_gen.dart
// 결과: database/seed_daily_verse.sql, backend/data/daily_verses.json
